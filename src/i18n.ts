function detectLocale(): Locale {
  // 1. Vite env override (for CLI testing: VITE_LANG=zh pnpm run tauri dev)
  const envLang = import.meta.env.VITE_LANG;
  if (envLang && envLang in translations) return envLang as Locale;

  // 2. User's saved preference
  const stored = localStorage.getItem("hc-locale");
  if (stored && stored in translations) return stored as Locale;

  // 3. System language
  const langs = navigator.languages ?? [navigator.language];
  for (const lang of langs) {
    const code = lang.toLowerCase().split("-")[0];
    if (code in translations) return code as Locale;
  }

  return "en";
}

let currentLocale: Locale = detectLocale();
const listeners = new Set<() => void>();

export function getLocale(): Locale {
  return currentLocale;
}

export function setLocale(locale: Locale) {
  currentLocale = locale;
  localStorage.setItem("hc-locale", locale);
  invoke("set_tray_locale", { locale }).catch(() => {});
  listeners.forEach((fn) => fn());
}

export function syncTrayLocale() {
  invoke("set_tray_locale", { locale: currentLocale }).catch(() => {});
}

export function t(key: string, params?: Record<string, string>): string {
  let text = translations[currentLocale]?.[key] ?? translations.en[key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      text = text.replace(`{${k}}`, v);
    }
  }
  return text;
}

export function useLocale(): Locale {
  const [, setTick] = useState(0);
  useEffect(() => {
    const listener = () => setTick((n) => n + 1);
    listeners.add(listener);
    return () => { listeners.delete(listener); };
  }, []);
  return currentLocale;
}
